<h5>HVAD ER MENNESKEHANDEL?</h5>

<p>Menneskehandel er en moderne form for slavehandel. Bagmændene eller – kvinderne bruger vold, tvang og/eller vildledning for at fjerne personer fra deres hjem og familie og tvinger dem til at arbejde mod deres vilje. Mennesker, det være sig voksne eller børn, bliver handlet mellem lande og indenfor deres eget land. Ofrene bliver tvunget til at arbejde som hushjælp, indenfor sexindustrien, som landarbejdere, fabriksarbejdere, gadesælgere etc.</p> 

<h5>Menneskehandel:</h5>

<p>Der er forskel på menneskesmugling og menneskehandel.<br/>
<p>En menneskesmugler vil sikre illegal indrejse til et land mod betaling, men ved ankomsten er den indsmuglede person fri og ser sædvanligvis ikke smugleren igen.
Ofrene for menneskehandel vil ikke nødvendigvis indrejse i et land illegalt.
Bagmændene eller kvinder kan have arrangeret legale rejsedokumenter og ofrene behøver derfor ikke at indrejse illegalt.</p>

<p>Forskellen på folk, der emigrerer eller er smuglet til et land, ligger også i hvad der sker ofrene for menneskehandel, når de ankommer til destinationsstedet.
Menneskehandel involverer altid trussel eller brug af vold, tvang og/eller vildledning for at tvinge ofrene til seksuel eller arbejdsmæssig udnyttelse.</p>

<p>Menneskehandel er i dag et verdensomspændende fænomen hvor kriminelle elementer på tværs af grænser er dybt involveret.
Menneskehandel er en lav-risiko kriminalitet med mulighed for en høj  profit. Kriminelle anklaget for narkotikahandel får ofte en strengere dom end dem der er anklaget for handel med mennesker, og det er alarmerende. De ser på levende mennesker som de ser på kokain i en plasticpose.
FN har estimeret, at menneskehandel generer ca. 10 milliarder US dollar om året.</p>

<p>Handel med Børn er en af de ondeste former for menneskehandel.</p>

<p>Problemet er størst i Vestafrika, Asien og Østeuropa, som leverer det største antal børn.  Vestafrikanske og østeuropæiske børn, som bliver genstand for menneskesmugling, ender hovedsageligt i sexindustrien eller som slaver med husligt arbejde. Kinesiske og vietnamesiske børn risikerer primært at blive tvunget ud i prostitution eller udgør billig arbejdskraft.</p>

<p>UNICEF oplyser, at nogle børn udnyttes i deres hjemland, men de fleste ender i Europa, hvor der er den største efterspørgsel. Nogle børn bortføres, andre lokkes fra fattige familier med løfte om penge, uddannelse eller en mulighed for at arbejde i et fjernt land.
De fleste familier, der sender deres børn ud for at tjene penge, forstår ikke, hvilke skrækkelige forhold deres børn vil komme til at leve under.
Rapporten ”Stop the Traffic” påpeger, at hundreder af kendte tilfælde af handel med børn kun er toppen af isbjerget. 
Indtil for ganske nylig var menneskehandel i mange lande kun ulovligt, hvis en handel udføres med henblik på seksuel udnyttelse.</p>

<p>I Afrika findes i dag ca. 10 millioner forældreløse børn grundet AIDS.
Bliver disse børn og unge ikke hjulpet og uddannet, vil de blive morgendagens slaver.  Af lande hvor de fleste børn lever kan nævnes: Nigeria, Etiopien, Congo, Kenya, Uganda, Tanzania, Zimbabwe, Sydafrika, og Zambia.</p>

<p>Hundredvis af mindreårige børn, fra 4-års alderen, bliver stadigvæk handlet over grænsen til the United Arab Emirates (UAE) hvor de bliver brugt som kamel jockeys, og det på trods af løfter om, at dette ikke ville forekomme efter 1.september 2002.</p> 

<p>Roden til Slaveri er FATTIGDOM og mangel på uddannelse.
Slaveri vil ikke stoppe før vi afhjælper fattigdommen og giver uddannelse en chance.
Uddannelse er magt – uden midler eller uddannelse er mennesker lette ofre for skrupelløse mænd og kvinder.</p>

<p>Talløse mennesker er i dag bundet i slaveri. Mennesker er mange steder en handelsvare (der kan købes og sælges). Hvor slaver tidligere af ejeren blev betragtet som en god investering der skulde holde længe, så er nutidens slaver omfattet af mentaliteten ”brug-og-smid-væk”.  Når en slave ikke mere kan give det forventede økonomiske udbytte, bliver slaven kasseret, ofte syg, og uden midler til at klare sig selv.</p>

<p>Mennesker er blevet en billig handelsvare, der i dag også kan bruges som reservedele (organ donation) eller som ofre ved okkulte ritualer.</p>

<p>Menneskehandel er det hurtigst voksende handelsområde under organiseret kriminalitet.</p>

<h5>MENNESKEHANDEL OG DANMARK:</h5>

<p>Danmark er endestation for ofre for menneskehandel fra Østeuropa og Asien, som ender op på bordeller eller massageklinikker. På trods af disse kendte problemer har den danske regering, ifølge en FN rapport udkommet den 21.maj 2003, rapporteret, at der ikke er nylig kendte forseelser vedrørende personer der er lokket til prostitution eller har prostitution som levevej.</p>

<p>Danmark har tiltrådt og ratificeret de fleste internationale konventioner, hvori indgår menneskehandel bl.a. FNs konvention om at forebygge, undertrykke og straffe menneskehandel, især med kvinder og børn.</p>

<p>Vedrørende FN protokollen om børns rettigheder har Danmark underskrevet den frivillige FN protokol om salg af børn, børneprostitution og børnepornografi, men mangler at ratificere protokollen, ligesom FNs internationale konvention om beskyttelse af alle migrerende personer og deres familier heller ikke er ratificeret.</p>

