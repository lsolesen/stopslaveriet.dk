<h5>DET MODERNE SLAVERI.</h5>
<p>Den gamle form for slaveri – og den nye.</p>
<p>Den gamle form for slaveri:<br/>
Slaver var dyre og der var få af dem, man passede på dem – de skulle gerne holde længe og formere sig, til glæde for kommende generationer. Man viste hvem de forskellige slaver tilhørte, for de var brændemærkede.</p>


<p>Den nye form for slaveri:<br/>
 Slaverne er billige, der er mange af dem, skyhøj fortjeneste – ingen omsorg  - når slaverne er slidt op – smides de bort  -  hvem ejer dem ?????  Det er der ingen der ved og det på tros af at der findes millioner af slaver.
 Det nye slaveri fokuserer udelukkende på profit, herredømme og kontrol over andre mennesker, så de kan tjene penge til én.
 I dag har man ikke officielt slave ejere men anonyme slaveholdere, og mellemmænd.</p>

<p>For første gang i menneskehedens historie er der en overflod af potentielle slaver, hvilket indebærer at prisen på slaver falder.  Den nye form for slaveri forekommer i dag i de fleste lande.</p>

<p>Mennesker er blevet en billig handelsvare, der i dag også kan bruges som Reservedele,
(organ donation, eller for okkulte ritualer).</p>

<p>Hvorfor er slaver så talrige og billige i dag?<br/>
<p>I Asien – Afrika – Sydamerika er der i dag 3 gange så mange mennesker som der var ved slutningen af den anden verdenskrig.
Millioner af mennesker bliver slaver grundet fattigdom – politisk uro – overbefolkning – mangel på uddannelse og viden om deres rettigheder som mennesker - men uanset hvad grunden er, så venter der disse mennesker et ondt og ydmygende liv.
I 1988 købte vi de første slaver – eller – rettere frikøbte en Kamayer familie i Nepal, og satte dem økonomisk i stand til at begynde et nyt liv. Det hedder selvfølgelig ikke køb/salg af slaver, men kontrakt fornyelse – men - uanset hvad det hedder så er det utrolig ubehageligt at man er stand til at købe mennesker.
Hvad er der sket siden I er blevet slaver? dertil forklarede manden at hans fader for 23 år siden havde lånt hvad der svare til ca.300 kr. af den lokale godsejer.  For de penge havde familien – faderen, moderen og deres 3 sønner plus deres koner og nu deres børn slavet for denne mand.
Dette er en ganske ”almindelig” historie som millioner af mennesker vil nikke genkendende til.</p>

<p>Uddannelse er magt – men millioner af børn får aldrig en chance for at lære at læse og skrive. På verdensplan er det sådan, at 1 ud af hver 4 voksne i udviklingslandene – og det er mange – mange mennesker – hverken kan læse og skrive – for de har aldrig haft en chance, det er ikke fordi de er dårligere begavet end andre mennesker.</p>

<p>I år 2002 lovede ikke mindre end 155 regeringer at tilbyde undervisning til alle senest i år 2015.
I dag ser de ud til, at de 88 fattigste lande ikke vil være i stand til at holde dette løfte.</p>

<p>I begyndelsen af halvfemserne samarbejdede vi med en lokal gruppe i Nepal vedrørende oprettelse af aftenskoler i landsbyerne i det sydvestlige hjørne af landet. 
Det drejede sig her om to etniske Thau grubber og Kamaiyerne (en betegnelse for gældsbundne).
Disse aftenskoler kom til at betyde et utroligt ”LØFT” for disse mennesker, de kom op på højde med de andre nepalesere.  Ydermere blev der bygget et børnehjem for forældreløse Kamayia børn for noget mere hjælpeløst end disse børn findes ikke.  Børnene blev tilknyttet den lokale skole..
Dette byggeri blev støttet af det norske Anti-Slaveri Selskab og Caritas Danmark.
Undervisningen blev støttet af DANIDA i en år række.</p>

