<h4>Hvad er ”trafficking”?</h4>

<p>Trafficking involverer flytning af folk bort fra de samfund som de lever i ved trusler eller ved brug af vold, bedrag eller tvang så de kan blive udnyttet som tvungne eller tvangsbundne (slavebundne) arbejdere.</p>

<p>Når børn bliver ”forflyttet” behøver der ikke at være vold, bedrag eller tvang involveret.  Blot det at flytte dem til fremmede steder hvor de bliver udnyttet er ”trafficking”.
På grund af de ”skjulte” metoder der bliver brugt ved ”trafficking” er det svært at sige hvor mange mennesker det drejer sig om.
En nylig Amerikansk rapport skønner at omkring 600.000 – 800.000 mennesker bliver smuglet over grænserne hvert år.</p>

<p>Anti-Slavery International, London.</p>
		
		
