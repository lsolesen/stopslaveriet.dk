<h5>Børneslaver / børnearbejdere.</h5>

<p>Der er i begyndelsen af dette århundrede millioner af børneslaver.<br/>
<p>Den internationale arbejdsorganisation ILO oplyser at der er millioner af børn i alderen fra 4 – 17 år der er tvunget til at arbejde og ofte udsættes for vold af den ene eller anden art.<br/>  
49 % af disse børn er piger.</p>

<p>Geografisk findes langt det største antal børneslaver / børnearbejdere i alderen 5-14 år i Asien fulgt af Afrika og Latinamerika. Et mindre antal findes i Nordamerika og EU, men her er antallet stærkt stigende.<br/> 
 Det er karakteristisk for børneslaver, at de bliver nægtet deres fundamentale rettigheder. Ydermere bliver de ofre skilt fra forældrene og i mange tilfælde flyttet fra deres land.</p>

<h5>Hvad kan børn bruges til?</h5>

<p>Landarbejde – Husarbejde- fremstilling af tæpper – trækul – tændstikker - mursten – soldater o.s.v og ikke mindst i sexindustrien.  Det gælder både piger og drenge.</p>

<p>Der er millioner af små ”børnehusslaver” eller endnu bedre – ”de usynlige slaver”, for hvem ser dem?  Det gør den husstand der enten ejer eller lejer dem, og som har fuld kontrol over dem. De har intet privatliv og mange bliver ødelagt fysisk og mentalt.  De kommer sjældent uden for det hus eller den lejlighed de arbejder i, og har derfor ingen kontakt med andre mennesker. Skolegang.
 eksisterer ikke for dem, men det gør hårdt arbejde fra tidlig morgen til sen aften.
Disse små mennesker – og det drejer sig både om piger og dreng, kommer i reglen til deres nye familie når de er ca. 6 år.   Der får de så et nyt navn, og glemmer hurtigt deres eget navn og baggrund.  Disse børn er meget ensomme, undertrykte, mishandlede, ofte pryglede og til tider seksuelt udnyttede.
Når disse børneslaver er så populærere skyldes det at de er så ”BILLIGE.”</p>

<p>Der er ikke megen litteratur om disse små mennesker – for hvem ser dem?</p>

<p>For få år siden tog en indisk organisation problemet op.    Det blev til en rystende rapport om undertrykkelse mishandling og selvmord.    Regeringen i Delhi var chokeret ikke mindst da det kom frem at mange af regeringens ansatte havde mindreårige husslaver.   Der er nu vedtaget en lov hvori der står ” Ingen mand eller kvinde i statens tjeneste må have børnearbejdere (under 14 år) i deres hjem.”.</p>

<p>Det er et stort fremskridt, som forhåbentlig vil brede sig til andre dele af Asien – Afrika og Sydamerika, hvor hovedparten af slaver befinder sig, men fænomenet findes også i EU.
Der er mere end 3000 husslaver i Paris, hvoraf 20 % befinder sig indenfor diplomatiet.  I Paris har politiet indført at alle diplomater skal oplyse hvor mange der befinder sig i den ”udvidede” familie som er indskrevet i diplomatens pas.</p>

<p>Tæppevævning :  En undersøgelse udført af Anti-slaveri International for få år siden , afslørede at 300.000 børn i alderen 6-14 år var beskæftiget i tæppe industrien i Asien. I mange af de små værksteder, som der findes utallige af, er belysningen dårlig og luften er tyk af støv og fnug fra uld og bomuld. 
Mange af disse børn var kidnappet, måske solgt eller arbejdede gammel gæld af fra deres forældre. (Børn arver jo forældrenes gæld). Det er ulykkelige børn der lider af hjemve, sult, mishandling og det både fysisk og psykisk så det er ikke underligt at selvmord forekommer.</p>

<p>I 1983 blev der lagt pres på den Indiske Tæppeindustri, for at få børn udskiftet med voksne arbejdere, og indført en mærkning der garanterede kunden, at tæppet var udført uden hjælp børneslaver/børnearbejdere.    Da pålidelighed, troværdighed og international legalisering af en sådan mærkning er nødvendigt, blev dette diskuteret i en komite bestående af internationale org. eksperter og fagfolk, tæppefabrikanter, eksportører, importører o.s.v.
Overenskomsten fik navnet ”Rugmark” Fundation og mærket hedder ”Rugmark”.</p>

<p>Ved en undersøgelse blandt det indre Københavns talrige tæppeforretninger fandt jeg kun 2 forretninger der kunne fremvise bevis for at de tæpper de forhandlede ikke var udført af børn. Et magert resultat, vi skal endelig ikke tro at vi er bedre end de andre – for det er vi ikke.</p>
 
<p>Børnesoldater:<br/>
Ifølge FNs statistik, arbejder ca. 300.000 børn under 18 år som regulære soldater, mens andre arbejder som guerilla-soldater, spioner og i nogle tilfælde endda som ”selvmordspiloter”.  Mange af børnesoldaterne er kidnappet.   En undersøgelse i El Salvador, Etiopien og Uganda viste at 1/3 af børnene var piger.</p>

<p>Børn misbrugt i sexindustrien:<br/> 
Børn er meget populære inden for sexindustrien, fordi man betragter dem som renere end voksne.
Specielt er jomfruer efterspurgt, da nogle mænd fejlagtigt tror at de vil blive helbredt for AIDS hvis de er sammen med en jomfru – det rene sludder.</p>  

<p>I Afrika findes i dag ca.10 millioner forældreløse børn grundet AIDS. Bliver disse børn og unge ikke hjulpet med skolegang og uddannelse, bliver de morgendagens slaver. Kan du ikke læse og skrive kan du udnyttes på det groveste – uden uddannelse er man magtesløs.</p>

<p>Hundredvis af mindreårige børn bliver stadigvæk handlet over grænsen til Unitted Arab Emirates, hvor de bruges som kamel jockeys, og det tiltros for løfter om at dette ikke vilde forekommer efter 1.september 2002.<br/>
Teori et – praksis noget andet.</p>

<p>Handel med børn er den ondeste former for menneskehandel og må og skal stoppes.</p>

