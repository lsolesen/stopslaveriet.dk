<h5>Hvad er en slave ?</h5>
<p>En slave er et menneske, der reelt ejes eller kontrolleres af andre, som har uindskrænket råderet over vedkommende med henblik på økonomisk udnyttelse.  Kontrollen udøves normalt gennem mental og fysisk mishandling eller trusler derom.  En slave har ingen personlige rettigheder eller frihed til at bestemme over egen krop og liv. Slaver kan handles, byttes og foræres bort.</p>

<p> I FNs Menneskerettighedserklæring af 1948 står der: Ingen må holdes i slaveri eller i trældom  -  Slaveri og slavehandel i alle former er forbudt.                                                                                                                                                                                              
Officielt er der ingen lande, der tillader slaveri, men i praksis ser det helt anderledes ud.</p>

<h5>Hvilken slags slaver findes i dag?</h5>

<p>Gældsslaver - Tvangsarbejder - Sexslaver - Børneslaver - Børnesoldater - Tvangsægteskaber.</p>

<p>Gældsslaver:<br/>
Mennesker bliver slavebundet når deres arbejde er krævet som betaling på et lån.. Der er utallige eks. på hvordan mennesker er blevet bundet i årevis, for blot få hundrede kroner. Når det er så svært at betale sin gæld tilbage, skyldes det tårnhøje renter der lægges på det lånte beløb. Det kan en slave ikke kontrollere fordi de i mange tilfælde – aldrig har gået i skole.
Over alt i verden findes der millioner af gældsslaver, der ofte er tvunget til at arbejde under forhold der er umenneskelige og hvor vold og trusler om vold er daglig kost.</p>

<p>For et par år siden foretog vi, en undersøgelse sammen med en lokal NGO, der viste at en mandlig gældsslaves arbejdsdag var på godt 17 timer i døgnet – ugens 7 dage. Hans kones lå på godt 13 timer, alle, ugens 7 dage, deres børns arbejds- indsats afhang af alder og køn.
 Denne undersøgelse blev foretaget i Asien, men var den foretaget i Afrika – Nord og Sydamerika eller i EU var resultatet blevet den samme.</p>

<p>Hvem skylder hvem penge?<br/>
Det er et simpelt regnskab: for eks. 17 timer pr. dag ugens 7 dage gange 20 års med en timeløn på 50 øre i timen (for ikke at overdrive) så er det et beløb der langt over skrider de få hundrede kr. som den gældsbundne har lånt, selv med grove renter. 
De gældsbundne/slaver i Nepal rejste sig i år 2000 og forlangte deres frihed, og takket en massiv opbakning fra udlandet fik de deres frihed.    Regeringen meddelte over TV og Radio at al gældsslaveri var ophævet fra dags dato ( 16-7 2000) men ikke et ord om at de slavebundne skulde have nogen form for kompensation for mange års slid og ydmygelser.
Det var ikke første gang at man proklamerede slaveriets ophævelse i Nepal, for det blev også gjort i 1923.   Samtidig med at slaveriet atter blev afskaffet i landet blev det strafbart at holde slaver, og det betød at de frigivne blev sat på gaden med dags varsel – og det midt i regntiden.
Og  der stod de så uden arbejde og penge, med kone og måske mange børn – og  hvad gør man så – man går tilbage og arbejder nu for en ussel løn, samtidig med at man må betale for at bo i et gammelt skur – eller man  kan dør af sult.</p>

<p>Da Indien i 1947 blev et selvstændigt land, proklamerede regeringen at al gammel gæld var eftergivet. Det medførte at tusinde af mennesker blev smidt på porten, og så kunne de jo komme tilbage og bede om arbejde for en ussel løn - hos deres gamle Herre.  Regeringerne har tilsyneladende intet lært. </p>

<p>Tvangsarbejdere:<br/>
Dette rammer mennesker, som er illegalt rekrutteret af regeringer, for eks. i Burma eller private mennesker og tvunget til at arbejde - som regel under trusler om vold eller anden straf.</p>

<p>Sexslaver:<br/>
 Mange bliver lokkede med gyldne løfter om et bedre tilværelse med fast og vellønnet 
Arbejde, men ender i slaveri, ofte i form af prostitution og eller fremstilling af pornografisk materiale.  Det er primært kvinder og børn – piger så vel som drenge – der handles med.    
I dag findes denne form for slaveri i hele verden.</p>

<p>Kontraktarbejdere:<br/> 
Arbejdskontrakter bruges ofte til at lokke mennesker i slaveri. Således er meget af det moderne slaveri skjult bag falske arbejdskontrakter.  Grunde strenge indvandrelove må de ofte gøre sig usynlige og er derfor helt i arbejdsgiverens magt og kan let udnyttes og mishandles.
Denne type slaveri vokser eksplosivt og er meget uigennemskueligt.</p>

