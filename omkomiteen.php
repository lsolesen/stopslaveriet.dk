<p>Komiteens målsætning er at bekæmpe slaveri og dets årsager, samt tage initiativer til at fremme       
  frigørelse af slaver herunder:</p>
<ul>
<li>At sprede oplysning om slaveriets omfang og art.</li>
<li>At deltage i projekter, der medvirker til at frigøre og rehabilitere slaver.</li>
<li>At påvirke politikere, organisationer og institutioner med henblik på bekæmpelse af slaver.</li>
<li>At deltage i nationalt og internationalt samarbejde med andre organisationer, der arbejder med samme mål.</li> 
</ul>

<p>Organisationens arbejde vil være i overensstemmelse med retningslinierne i FNs Menneskerettighedserklæring af 1948.</p>

<ul>
<li>Ingen må holdes i slaveri eller trældom</li>
<li>Slaveri og slavehandel i alle former er forbudt.</li>
</ul>

<p>Dansk Komite mod Slaveri er en upolitisk, uafhængig dansk forening, der blev etableret i foråret 2000.
Komiteen samarbejder med andre Anti-slaveri org. verden over.</p>



<h5>Hvad kan vi gøre for at standse det voksne globale slaveri?</h5>
<ul>
<li>Det er nødvendigt at finde metoder, ved hjælp af hvilke man kan tvinge de forskellige lande til  at respektere FNs  Menneskerettighedserklæring af 1948.</li>

<li>De internationale hjælpeorganisationer og bilaterale donororganisationer, der giver bestand til udviklingslandene, bør i større udstrækning, end er tilfældet i dag, stille krav om at Menneskerettighedserklæringen skal overholdes, og det bør fremhæves, at en betingelse for udviklingsbistand er, at slaveri afskaffes, formelt såvel som reelt.</li>

<li>Desuden bør førstnævnte organisationer i større grad være aktive med at udvikle og støtte projekter, der både frigør og hjælper slaver i gang med en udvikling, der kan sætte dem i stand til at forsørge sig selv og deres familier.</li>

<li>Det er vigtigt at udbrede kendskab til slaveriets eksistens og omfang og skabe folkelig grobund for at slaveri, én gang for alle må ophøre. </li>

<li>Professor Kevin Bales skriver: Penge er nødvendig i denne kamp. Hvis man havde det beløb til rådighed som et jetfly eller et krigsskib koster, ville slaveriet blive reduceret dramatisk.  Vi har alle kendskab til FNs våbeninspektørernes arbejde vedrørende masseødelæggelses våben, hvor er FNs Slaveri- inspektører? Var samme energi brugt på sidstnævnte ville der ske store forandringer.</li>
</ul>

<h5>Hvad gør vi netop nu?</h5>

<ul>
<li>
En Norsk politimester er netop valgt som leder af Interpols arbejdsgruppe bestående af  
Politifolk fra 188 lande. Disse samarbejder for at stoppe handlen med kvinder og børn over  
landegrænser i Europa, Asien, Afrika og Amerika. Det anslås at det drejer sig om mere end 
10 millioner børn der udnyttes på det internationale sex-marked.  (Vårt Land Norge)</li>

<li>Love der forbyder menneskehandel er i sommer blevet ratificeret i både Rusland og          
Georgien.  Menneskehandlere kan idømmes fængselsstraffe helt op til 20 år.</li>

<li>Brasiliens præsident har vist sig at være en handlekraftig politiker der ikke bare taler men     
også handler. Talrige slaver er netop frigivet i det nordøstlige hjørne af landet. Præsidenten  
anslår at der i landet lever mere end 25.000 slavebundne mennesker er umenneskelige forhold.</li>

<li>Foredrag om det ”Det moderne slaveri” fortsætter landet over.</li>

</ul>


